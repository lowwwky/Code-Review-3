// Конструктор графа
// Назначение: создает граф с заданным количеством вершин
// Параметры:
//   amount_of_vertices - количество вершин в графе (должно быть положительным)
// Инициализирует:
//   vertices - сохраняет количество вершин
//   adjacency_matrix - создает матрицу смежности размером vertices x vertices,
//                      заполненную нулями (отсутствие ребер)
// Исключения:
//   std::invalid_argument - если количество вершин <= 0
// Примечание: создается пустой граф без ребер. Ребра добавляются через set_edge()
Graph::Graph(int amount_of_vertices) : vertices(amount_of_vertices) {
    if (amount_of_vertices <= 0) {
        throw std::invalid_argument("Количество вершин должно быть положительным");
    }

    adjacency_matrix.resize(vertices, std::vector<int>(vertices, 0));
}


// Метод добавления или обновления ребра
// Назначение: устанавливает вес ребра между двумя вершинами
// Параметры:
//   from - индекс начальной вершины (0-based)
//   to - индекс конечной вершины (0-based)
//   weight - вес ребра (ненулевое значение означает наличие ребра)
// Исключения:
//   std::out_of_range - если from или to выходят за пределы [0, vertices-1]
// Примечание: 
//   - Для добавления ребра достаточно вызвать set_edge(from, to, weight)
//   - Вес 0 интерпретируется как отсутствие ребра
//   - Для неориентированных графов требуется вызывать дважды:
//     set_edge(u, v, w) и set_edge(v, u, w)
void Graph::set_edge(int from, int to, int weight) {
    if (from < 0 || from >= vertices || to < 0 || to >= vertices) {
        throw std::out_of_range("Индекс вершины вне диапазона");
    }

    adjacency_matrix[from][to] = weight;
}

// Метод получения веса ребра
// Назначение: возвращает вес ребра между двумя вершинами
// Параметры:
//   from - индекс начальной вершины (0-based)
//   to - индекс конечной вершины (0-based)
// Возвращает:
//   int - вес ребра (0 означает отсутствие ребра)
// Исключения:
//   std::out_of_range - если from или to выходят за пределы [0, vertices-1]
// Примечание: метод объявлен как const, что позволяет вызывать его
//            для константных объектов Graph
int Graph::get_edge(int from, int to) const {
    if (from < 0 || from >= vertices || to < 0 || to >= vertices) {
        throw std::out_of_range("Индекс вершины вне диапазона");
    }

    return adjacency_matrix[from][to];
}

// Метод получения количества вершин
// Назначение: возвращает количество вершин в графе
// Возвращает:
//   int - количество вершин
// Примечание: метод объявлен как const, используется для получения
//            информации о размере графа без его изменения
int Graph::get_vertices_count() const {
    return vertices;
}


// Метод получения константной ссылки на матрицу смежности
// Назначение: предоставляет прямой доступ к матрице смежности для
//            эффективного обхода или анализа графа
// Возвращает:
//   const std::vector<std::vector<int>>& - константная ссылка на матрицу смежности
// Примечания:
//   - Возвращает константную ссылку, предотвращая модификацию графа
//   - Позволяет вызывающему коду избежать многократных вызовов get_edge()
//   - Используется в алгоритмах, которым нужен быстрый доступ к матрице
const std::vector<std::vector<int>>& Graph::get_adjacency_matrix() const {
    return adjacency_matrix;
}

// Метод очистки всех ребер
// Назначение: удаляет все ребра из графа, сохраняя количество вершин
// Алгоритм: обходит все строки матрицы смежности и заполняет их нулями
// Примечания:
//   - После вызова граф становится пустым (без ребер)
//   - Количество вершин не изменяется
//   - Более эффективен, чем создание новой матрицы, так как переиспользует память
void Graph::clear() {
    for (auto& row : adjacency_matrix) {
        std::fill(row.begin(), row.end(), 0);
    }
}