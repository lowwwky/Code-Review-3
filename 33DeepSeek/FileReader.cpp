#include "FileReader.h"
#include <fstream>
#include <sstream>
#include <stdexcept>
#include <iostream>

// Конструктор класса FileReader
// Назначение: инициализирует объект для чтения графа из файла
// Параметры:
//   fileName - имя файла, содержащего матрицу смежности графа
// Инициализирует:
//   file_name - сохраняет имя файла для последующего использования
// Примечание: конструктор не открывает файл, а только сохраняет имя.
//            Фактическое открытие и чтение происходит при вызове readGraph()
FileReader::FileReader(const std::string& fileName) : file_name(fileName) {
}

// Назначение: открывает файл, читает матрицу смежности и создает объект Graph
// Алгоритм работы:
//   1. Открывает файл с сохраненным именем
//   2. Проверяет успешность открытия (если нет - выбрасывает исключение)
//   3. Читает количество вершин n из первой строки
//   4. Проверяет корректность прочитанного значения (положительное число)
//   5. Создает объект Graph с n вершинами
//   6. Построчно читает матрицу смежности размером n x n
//   7. Для каждого ненулевого элемента добавляет ребро в граф
//   8. Закрывает файл и возвращает созданный граф
// Возвращает:
//   Graph - объект графа, построенный на основе данных из файла
// Исключения:
//   std::runtime_error - если файл не открыт, ошибка чтения количества вершин,
//                        количество вершин не положительное, или ошибка чтения матрицы
// Формат входного файла:
//   Первая строка: количество вершин (целое положительное число)
//   Последующие n строк: матрица смежности n x n (целые числа, разделенные пробелами)
//   Нулевые элементы означают отсутствие ребра, ненулевые - вес ребра
Graph FileReader::read_graph() {
    std::ifstream file(file_name);

    if (!file.is_open()) {
        throw std::runtime_error("Не удалось открыть файл: " + file_name);
    }

    // FIX_ME: в Google Codestyle Guide названия переменных пишутся как lower_case_with_underscores.
    /*int n;*/
    int amount_of_vertax;
    file >> amount_of_vertax;

    if (file.fail()) {
        throw std::runtime_error("Ошибка чтения количества вершин");
    }

    if (amount_of_vertax <= 0) {
        throw std::runtime_error("Количество вершин должно быть положительным");
    }

    Graph graph(amount_of_vertax);

    for (int i = 0; i < amount_of_vertax; ++i) {
        for (int j = 0; j < amount_of_vertax; ++j) {
            int weight;
            file >> weight;

            if (file.fail()) {
                throw std::runtime_error("Ошибка чтения элемента матрицы смежности");
            }

            if (weight != 0) {
                graph.set_edge(i, j, weight);
            }
        }
    }

    file.close();
    return graph;
}

// Метод установки имени файла
// Назначение: изменяет имя файла для чтения графа
// Параметры:
//   fileName - новое имя файла
// Используется для перенастройки существующего объекта FileReader
// на чтение из другого файла без создания нового объекта
void FileReader::set_file_name(const std::string& fileName) {
    file_name = fileName;
}

// Метод получения имени файла
// Назначение: возвращает текущее имя файла, из которого будет производиться чтение
// Возвращает:
//   std::string - имя файла, сохраненное в объекте
// Используется для проверки текущей конфигурации объекта
std::string FileReader::get_file_name() const {
    return file_name;
}