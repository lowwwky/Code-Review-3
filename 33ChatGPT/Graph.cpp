#include "Graph.h"
#include <fstream>
#include <iostream>

// Конструктор по умолчанию класса Graph
// Назначение: создает пустой граф без вершин
// Инициализирует:
//   amount_of_vertices - количество вершин (устанавливается в 0)
//   adj_matrix - матрица смежности (остается пустой)
// Примечание: граф создается в неинициализированном состоянии.
//            Для загрузки данных необходимо вызвать load_from_file()
Graph::Graph() : amount_of_vertices(0) {}

// Метод загрузки графа из файла
// Назначение: читает граф из текстового файла, содержащего матрицу смежности
// Параметры:
//   filename - имя файла для загрузки
// Возвращает:
//   bool - true, если загрузка выполнена успешно
//          false, если возникла ошибка (файл не открыт)
// Формат файла:
//   Первая строка: количество вершин (целое число)
//   Последующие строки: матрица смежности размером n x n
//                       (целые числа, разделенные пробелами или переводами строк)
// Алгоритм работы:
//   1. Открывает файл для чтения
//   2. Проверяет успешность открытия (если нет - выводит сообщение об ошибке и возвращает false)
//   3. Считывает количество вершин
//   4. Изменяет размер матрицы смежности до n x n
//   5. Построчно считывает значения матрицы смежности
//   6. Закрывает файл
//   7. Возвращает true
// Примечания:
//   - При ошибке открытия файла выводит сообщение в std::cout
//   - Не проверяет корректность считанных данных (например, отрицательные значения)
//   - Предыдущее содержимое графа полностью заменяется
bool Graph::load_from_file(const std::string& filename) {
    std::ifstream file(filename);
    if (!file.is_open()) {
        std::cout << "Ошибка открытия файла\n";
        return false;
    }

    file >> amount_of_vertices;
    adj_matrix.resize(amount_of_vertices, std::vector<int>(amount_of_vertices));

    for (int i = 0; i < amount_of_vertices; i++) {
        for (int j = 0; j < amount_of_vertices; j++) {
            file >> adj_matrix[i][j];
        }
    }

    file.close();
    return true;
}

// Метод получения количества вершин
// Назначение: возвращает количество вершин в графе
// Возвращает:
//   int - количество вершин (0 для пустого графа)
// Примечание: метод объявлен как const, что позволяет вызывать его
//            для константных объектов Graph
int Graph::get_size() const {
    return amount_of_vertices;
}

// Метод получения константной ссылки на матрицу смежности
// Назначение: предоставляет прямой доступ к матрице смежности для
//            эффективного обхода или анализа графа
// Возвращает:
//   const std::vector<std::vector<int>>& - константная ссылка на матрицу смежности
// Примечания:
//   - Возвращает константную ссылку, предотвращая модификацию графа извне
//   - Позволяет алгоритмам (например, топологической сортировки) эффективно
//     обращаться к матрице без копирования
//   - Использование ссылки избегает лишнего копирования данных
const std::vector<std::vector<int>>& Graph::get_matrix() const {
    return adj_matrix;
}