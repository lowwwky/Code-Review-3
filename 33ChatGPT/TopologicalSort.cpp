// Конструктор класса TopologicalSort
// Назначение: инициализирует объект для выполнения топологической сортировки графа
// Параметры:
//   searched_graph - константная ссылка на граф, который будет отсортирован
// Инициализирует:
//   graph - сохраняет ссылку на граф для последующей обработки
//   has_cycle - флаг наличия цикла в графе (инициализируется false)
//   visited - вектор состояний вершин (размер = количество вершин, все 0)
// Состояния visited:
//   0 - вершина не посещена
//   1 - вершина в процессе обработки (в стеке рекурсии)
//   2 - вершина полностью обработана
// Примечание: конструктор не выполняет сортировку, только подготавливает структуры данных.
//            Фактическая сортировка происходит при вызове метода sort()
TopologicalSort::TopologicalSort(const Graph& searched_graph)
    : graph(searched_graph), has_cycle(false) {
    visited.resize(graph.get_size(), 0);
}

