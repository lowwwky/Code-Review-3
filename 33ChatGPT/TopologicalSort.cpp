// Рекурсивный метод обхода в глубину (DFS) для построения топологического порядка
// Назначение: выполняет рекурсивный обход графа, используя цветовую маркировку вершин
//            для обнаружения циклов и построения порядка
// Параметры:
//   verticles - индекс текущей вершины для обработки
// Алгоритм (использует 3-цветную маркировку):
//   1. Если уже обнаружен цикл - немедленно возвращается
//   2. Отмечает вершину как "в процессе обработки" (цвет 1)
//   3. Обходит всех соседей в порядке убывания индексов (от n-1 до 0)
//   4. Для каждого соседа:
//      - Если не посещен (0) - рекурсивный вызов dfs()
//      - Если в процессе обработки (1) - обнаружен цикл
//   5. После обработки всех соседей отмечает вершину как "обработанную" (цвет 2)
//   6. Добавляет вершину в вектор результата
// Примечание: обход соседей в порядке убывания обеспечивает детерминированный результат
void TopologicalSort::dfs(int verticles) {
    if (has_cycle) return;

    visited[verticles] = 1;

    const auto& matrix = graph.get_matrix();
    int amount_of_vertices = graph.get_size();

    // Перебор в порядке убывания
    for (int i = amount_of_vertices - 1; i >= 0; i--) {
        if (matrix[verticles][i] != 0) {
            if (visited[i] == 0) {
                dfs(i);
            }
            else if (visited[i] == 1) {
                has_cycle = true;
                return;
            }
        }
    }

    visited[verticles] = 2;
    result.push_back(verticles);
}

// Основной метод выполнения топологической сортировки
// Назначение: выполняет полный алгоритм топологической сортировки:
//            1. Запускает DFS для всех непосещенных вершин
//            2. Проверяет наличие цикла
//            3. При успехе разворачивает результат
// Возвращает:
//   bool - true, если топологическая сортировка выполнена успешно (граф ацикличен)
//          false, если граф содержит цикл
// Алгоритм:
//   1. Обходит все вершины в порядке убывания индексов (от n-1 до 0)
//   2. Для каждой непосещенной вершины запускает dfs()
//   3. Если обнаружен цикл (has_cycle == true), возвращает false
//   4. Разворачивает результат (так как dfs добавляет вершины в обратном порядке)
//   5. Возвращает true
// Примечание: обход вершин в порядке убывания обеспечивает детерминированный
//            результат при наличии нескольких возможных топологических порядков
bool TopologicalSort::sort() {
    int amount_of_vertices = graph.get_size();

    // запуск DFS тоже в порядке убывания
    for (int i = amount_of_vertices - 1; i >= 0; i--) {
        if (visited[i] == 0) {
            dfs(i);
        }
    }

    if (has_cycle) return false;

    std::reverse(result.begin(), result.end());
    return true;
}

// Метод вывода результата топологической сортировки
// Назначение: выводит топологический порядок вершин в консоль
// Формат вывода:
//   - Вершины разделены пробелами
//   - Номера вершин выводятся с 1 (result[i] + 1)
//   - В конце выводится символ новой строки
// Пример вывода: "1 3 2 4"
// Примечание: метод предполагает, что sort() был успешно вызван перед этим.
//            Если сортировка не выполнялась или граф содержит цикл,
//            результат может быть пустым или некорректным
void TopologicalSort::print_result() const {
    for (int v : result) {
        std::cout << v + 1 << " "; // вывод с 1
    }
    std::cout << std::endl;
}