// Конструктор узла выражения с указанием типа и значения
// Назначение: создает узел абстрактного синтаксического дерева (AST) для математического выражения
//            с явным указанием типа узла и его строкового представления
// Параметры:
//   type - тип узла (NodeType::NUMBER, NodeType::VARIABLE или NodeType::OPERATOR)
//   value - строковое представление значения узла (например, "5", "x", "+")
// Инициализирует:
//   type - сохраняет тип узла для быстрого определения категории
//   value - строковое представление (используется при выводе и сравнении)
//   number_value - числовое значение (инициализируется 0, так как для переменных и операторов
//                  числовое значение не определено)
//   left - указатель на левый дочерний узел (инициализируется nullptr)
//   right - указатель на правый дочерний узел (инициализируется nullptr)
// Примечание: этот конструктор удобен для создания узлов-операторов и узлов-переменных,
//            где числовое значение не требуется или неизвестно
ExpressionNode::ExpressionNode(NodeType type, std::string value)
    : type(type), value(value), number_value(0), left(nullptr), right(nullptr) {
}

// FIX_ME: в Google Codestyle Guide названия переменных пишутся как lower_case_with_underscores.
// Конструктор узла-числа
// Назначение: создает узел, представляющий числовое значение, с автоматическим
//            определением типа как NodeType::NUMBER
// Параметры:
//   value - целочисленное значение числа
// Инициализирует:
//   type - устанавливается в NodeType::NUMBER (числовой узел)
//   value - строковое представление числа (получается через std::to_string)
//   number_value - сохраняет числовое значение для быстрого доступа без преобразования строк
//   left - указатель на левый дочерний узел (nullptr, так как число - листовой узел)
//   right - указатель на правый дочерний узел (nullptr, так как число - листовой узел)
// Преимущество: сохраняет как строковое, так и числовое представление,
//              что ускоряет вычисления (не требуется преобразование строки в число)
ExpressionNode::ExpressionNode(int value)
    : type(NodeType::NUMBER), value(std::to_string(value)), number_value(value), left(nullptr), right(nullptr) {
}

// Деструктор узла выражения
// Назначение: освобождает всю память, выделенную для поддерева, корнем которого является данный узел
// Алгоритм работы:
//   1. Рекурсивно удаляет левое поддерево (delete left вызывает деструктор левого узла,
//      который в свою очередь удаляет свои левого и правого потомков)
//   2. Рекурсивно удаляет правое поддерево (аналогично)
//   3. После выполнения деструктора самого узла память, выделенная для объекта, освобождается
// Примечание: используется рекурсивное удаление, что корректно для бинарного дерева.
//            Важно: требуется, чтобы узлы создавались через new, а не через массивы или размещающий new.
//            Удаление nullptr безопасно (delete nullptr не выполняет никаких действий)
ExpressionNode::~ExpressionNode() {
    delete left;
    delete right;
}