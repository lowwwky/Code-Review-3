// Конструктор графа с загрузкой из файла
// Назначение: создает граф и немедленно загружает данные из указанного файла
// Параметры:
//   file_name - имя файла, содержащего матрицу смежности графа
// Инициализирует:
//   amount_of_vertices - количество вершин (устанавливается после чтения из файла)
//   has_cycle - флаг наличия цикла в графе (инициализируется false)
//   adj_matrix - матрица смежности (заполняется из файла)
//   visited - вектор состояний вершин для DFS (размер = amount_of_vertices, все 0)
//   result - вектор для хранения результата топологической сортировки (пока пуст)
// Исключения:
//   std::runtime_error - если не удалось открыть файл
// Формат файла:
//   Первая строка: количество вершин (целое число)
//   Последующие строки: матрица смежности размером n x n
// Примечание: конструктор объединяет создание объекта и загрузку данных,
//            что гарантирует, что граф всегда находится в валидном состоянии
//            после создания
Graph::Graph(const std::string& file_name) : amount_of_vertices(0), has_cycle(false) {
    std::ifstream file(file_name);
    if (!file.is_open()) {
        throw std::runtime_error("Could not open file");
    }

    file >> amount_of_vertices;
    adj_matrix.resize(amount_of_vertices, std::vector<int>(amount_of_vertices));
    visited.resize(amount_of_vertices, 0);

    for (int i = 0; i < amount_of_vertices; ++i) {
        for (int j = 0; j < amount_of_vertices; ++j) {
            file >> adj_matrix[i][j];
        }
    }
    file.close();
}

