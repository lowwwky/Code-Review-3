// Рекурсивный метод обхода в глубину (DFS) для топологической сортировки
// Назначение: выполняет рекурсивный обход графа, используя 3-цветную маркировку
//            для обнаружения циклов и построения топологического порядка
// Параметры:
//   vertices - индекс текущей вершины для обработки (0-based)
// Алгоритм (использует 3-цветную маркировку):
//   1. Отмечает вершину как "в процессе обработки" (цвет 1 - серый)
//   2. Перебирает всех соседей в порядке убывания индексов (от n-1 до 0)
//   3. Для каждого соседа, если есть ребро:
//      - Если сосед имеет статус 1 (в обработке) → обнаружен цикл
//      - Если сосед не посещен (статус 0) → рекурсивный вызов dfs()
//   4. После обработки всех соседей отмечает вершину как "завершенную" (цвет 2 - черный)
//   5. Добавляет вершину в результат (с преобразованием к 1-based нумерации)
// Примечания:
//   - Обход соседей в порядке убывания обеспечивает детерминированный результат
//   - Использует поле has_cycle класса для сигнализации об обнаружении цикла
//   - При обнаружении цикла немедленно прекращает дальнейшую обработку
void Graph::dfs(int vertices) {
    visited[vertices] = 1; // Помечаем как "в обработке" (серая)

    // Перебираем соседей в порядке убывания номеров (от n-1 до 0)
    for (int neighbor = amount_of_vertices - 1; neighbor >= 0; --neighbor) {
        if (adj_matrix[vertices][neighbor] != 0) {
            if (visited[neighbor] == 1) {
                has_cycle = true;
                return;
            }
            if (visited[neighbor] == 0) {
                dfs(neighbor);
            }
        }
    }

    visited[vertices] = 2; // Помечаем как "завершена" (черная)
    result.push_back(vertices + 1); // +1, так как вершины обычно нумеруются с 1
}

// Основной метод выполнения топологической сортировки
// Назначение: выполняет полный алгоритм топологической сортировки графа:
//            1. Сбрасывает состояние
//            2. Запускает DFS для всех вершин
//            3. Проверяет наличие цикла
//            4. При успехе разворачивает результат
// Возвращает:
//   bool - true, если топологическая сортировка выполнена успешно (граф ацикличен)
//          false, если граф содержит цикл
// Алгоритм:
//   1. Очищает вектор результата
//   2. Сбрасывает все флаги visited в 0
//   3. Сбрасывает флаг has_cycle
//   4. Запускает DFS для всех вершин в порядке убывания индексов (от n-1 до 0)
//   5. После каждого вызова dfs() проверяет, не обнаружен ли цикл
//   6. Если цикл обнаружен, возвращает false
//   7. Разворачивает результат (так как dfs добавляет вершины в обратном порядке)
//   8. Возвращает true
// Примечания:
//   - Обход вершин в порядке убывания обеспечивает детерминированный результат
//   - Результат сохраняется в поле result для последующего вывода
bool Graph::topological_sort() {
    result.clear();
    std::fill(visited.begin(), visited.end(), 0);
    has_cycle = false;

    // Запускаем DFS для всех вершин в порядке убывания номеров
    for (int i = amount_of_vertices - 1; i >= 0; --i) {
        if (visited[i] == 0) {
            dfs(i);
            if (has_cycle) return false;
        }
    }

    // В DFS топологический порядок получается обратным
    std::reverse(result.begin(), result.end());
    return true;
}

// Метод вывода результата топологической сортировки
// Назначение: выводит топологический порядок вершин в консоль
// Формат вывода:
//   - Вершины разделены пробелами
//   - Номера вершин выводятся с 1 (как сохранено в result)
//   - Между последним элементом и символом новой строки нет лишнего пробела
// Пример вывода: "1 3 2 4"
// Примечания:
//   - Метод объявлен как const, так как не изменяет состояние объекта
//   - Предполагает, что topological_sort() был успешно вызван перед этим
//   - Если сортировка не выполнялась или граф содержит цикл, результат может быть пустым
void Graph::print_result() const {
    for (size_t i = 0; i < result.size(); ++i) {
        std::cout << result[i] << (i == result.size() - 1 ? "" : " ");
    }
    std::cout << std::endl;
}