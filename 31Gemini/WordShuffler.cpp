#include "WordShuffler.h"
#include <iostream>
#include <algorithm>
#include <random>


// Конструктор класса WordShuffler
// Назначение: инициализирует объект целевым словом, устанавливает начальное состояние
// Параметры:
//   target - эталонное слово, к которому нужно прийти в процессе перемешивания
// Инициализирует:
//   target_word - сохраняет целевое слово для последующего сравнения
//   current_word - текущее слово, изначально равно целевому
//   attempts - счетчик попыток перемешивания, начинается с 0
WordShuffler::WordShuffler(const std::string& target)
    : target_word(target), current_word(target), attempts(0) {
}

// Метод однократного перемешивания
// Назначение: выполняет случайную перестановку символов текущего слова
// Особенности:
//   - Использует статические объекты random_device и mt19937 для обеспечения
//     качественной случайности между вызовами
//   - static сохраняет состояние генератора между вызовами метода,
//     что обеспечивает более равномерное распределение случайных чисел
//   - random_device используется для получения энтропии (недетерминированного seed)
//   - mt19937 - генератор Mersenne Twister, обеспечивающий высокое качество
//     случайной последовательности
//   - std::shuffle выполняет равномерную случайную перестановку всех символов
void WordShuffler::shuffleOnce() {
    // Инициализируем генератор случайных чисел
    static std::random_device randomized;
    static std::mt19937 generator(randomized());

    // Перемешиваем текущую строку
    std::shuffle(current_word.begin(), current_word.end(), generator);
}

// Основной метод, запускающий процесс перемешивания
// Назначение: многократно перемешивает слово до тех пор, пока случайно
//            не получит исходное целевое слово
// Алгоритм работы:
//   1. Проверяет, что целевое слово не пустое (защита от некорректных данных)
//   2. В цикле do-while:
//      - Выполняет однократное перемешивание (shuffleOnce)
//      - Увеличивает счетчик попыток
//      - Выводит полученное слово на экран
//      - Продолжает, пока текущее слово не совпадет с целевым
//   3. По завершении выводит итоговое количество попыток
// Важно: используется do-while, а не while, чтобы выполнить хотя бы одно
//        перемешивание (если бы использовался while, при совпадении
//        изначальных слов цикл не выполнился бы ни разу)
void WordShuffler::run() {
    if (target_word.empty()) return;

    do {
        shuffleOnce();
        attempts++;
        std::cout << current_word << std::endl;
    } while (current_word != target_word);

    std::cout << attempts << " attempts" << std::endl;
}