#include "Node.h"
#include <cctype>


// Конструктор класса Node
// Назначение: инициализирует узел абстрактного синтаксического дерева (AST)
// Параметры:
//   val - строковое значение узла (может быть числом, переменной 'x' или оператором)
// Инициализирует:
//   value - сохраняет строковое представление узла
//   left - указатель на левый дочерний узел (инициализируется nullptr)
//   right - указатель на правый дочерний узел (инициализируется nullptr)
// Примечание: для листовых узлов (числа и переменные) left и right остаются nullptr,
//            для внутренних узлов (операторы) эти указатели будут установлены позже
//            при построении дерева
Node::Node(const std::string& val) : value(val), left(nullptr), right(nullptr) {}

// Метод проверки, является ли узел оператором
// Назначение: определяет, представляет ли данный узел арифметическую операцию
// Возвращает:
//   true - если value равно одному из операторов: +, -, *, /, %, ^
//   false - в противном случае (число, переменная или неизвестный токен)
// Используется при обходе и вычислении дерева для различения типов узлов
// Примечание: оператор '^' представляет возведение в степень
bool Node::is_operator() const {
    return value == "+" || value == "-" || value == "*" ||
        value == "/" || value == "%" || value == "^";
}

// Метод проверки, является ли узел числом
// Назначение: определяет, представляет ли данный узел числовое значение
// Возвращает:
//   true - если первый символ value является цифрой (поддерживает многозначные числа)
//   false - в противном случае
// Алгоритм: проверяет только первый символ строки, что достаточно, так как
//           все числа в дереве хранятся как строки, начинающиеся с цифры
//           (отрицательные числа не поддерживаются в данной реализации)
// Используется при вычислении выражения для получения числовых значений
bool Node::is_number() const {
    return std::isdigit(value[0]);
}

// Метод проверки, является ли узел переменной
// Назначение: определяет, представляет ли данный узел переменную 'x'
// Возвращает:
//   true - если value равно строке "x"
//   false - в противном случае
// Используется при вычислении выражения для подстановки значения переменной
// Примечание: в данной реализации поддерживается только одна переменная 'x',
//             что характерно для математических выражений с одним параметром
bool Node::is_variable() const {
    return value == "x";
}