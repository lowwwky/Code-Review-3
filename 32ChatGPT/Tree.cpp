// Метод вычисления значения выражения, представленного деревом
// Назначение: рекурсивно вычисляет результат математического выражения
//            при заданном значении переменной x
// Параметры:
//   node - умный указатель на текущий узел дерева (shared_ptr для автоматического управления памятью)
//   x_value - целочисленное значение переменной x для подстановки
// Возвращает: целочисленный результат вычисления поддерева
// Алгоритм работы:
//   1. Базовые случаи:
//      - nullptr → возвращает 0 (защита от некорректных указателей)
//      - узел-число → преобразует строку в целое число и возвращает
//      - узел-переменная → возвращает переданное значение x_value
//   2. Рекурсивный случай (узел-оператор):
//      - Рекурсивно вычисляет левое и правое поддеревья
//      - Применяет соответствующую операцию к полученным значениям
// Поддерживаемые операции: +, -, *, /, %, ^ (возведение в степень)
// Примечание: использует std::pow для возведения в степень
int Tree::evaluate(std::shared_ptr<Node> node, int x_value) {
    if (!node) return 0;

    if (node->is_number()) return std::stoi(node->value);
    if (node->is_variable()) return x_value;

   /* int l = evaluate(node->left, x_value);
    int r = evaluate(node->right, x_value);*/
    int left_node = evaluate(node->left, x_value);
    int right_node = evaluate(node->right, x_value);

    if (node->value == "+") return left_node + right_node;
    if (node->value == "-") return left_node - right_node;
    if (node->value == "*") return left_node * right_node;
    if (node->value == "/") return left_node / right_node;
    if (node->value == "%") return left_node % right_node;
    if (node->value == "^") return (int)pow(left_node, right_node);

    return 0;
}

// Метод визуализации дерева в консоли (повернутое на 90° дерево)
// Назначение: выводит структуру дерева в удобочитаемом виде, где корень находится слева,
//            правое поддерево отображается выше левого
// Параметры:
//   node - умный указатель на текущий узел
//   space - количество пробелов для отступа (увеличивается с каждым уровнем вложенности)
// Алгоритм:
//   1. Рекурсивно обходит дерево в порядке: правое поддерево → текущий узел → левое поддерево
//   2. Увеличивает отступ на 5 пробелов на каждом уровне глубины
//   3. Выводит значение узла с соответствующим отступом
void Tree::print_tree(std::shared_ptr<Node> node, int space) {
    if (!node) return;

    space += 5;
    print_tree(node->right, space);

    for (int i = 5; i < space; i++) std::cout << " ";
    std::cout << node->value << "\n";

    print_tree(node->left, space);
}

// Метод записи дерева в префиксной форме (польская запись) в файл
// Назначение: обходит дерево в порядке: корень → левое поддерево → правое поддерево
//            и записывает значения узлов в файл
// Параметры:
//   node - умный указатель на текущий узел
//   out - ссылка на выходной файловый поток (ofstream)
// Формат вывода: оператор левый_операнд правый_операнд
// Пример: + * 2 x 3  (для выражения 2*x+3)
// Используется для сериализации дерева или вывода в формате, удобном для интерпретаторов
void Tree::to_prefix(std::shared_ptr<Node> node, std::ofstream& out) {
    if (!node) return;
    out << node->value << " ";
    to_prefix(node->left, out);
    to_prefix(node->right, out);
}

// Метод записи дерева в постфиксной форме (обратная польская запись) в файл
// Назначение: обходит дерево в порядке: левое поддерево → правое поддерево → корень
//            и записывает значения узлов в файл
// Параметры:
//   node - умный указатель на текущий узел
//   out - ссылка на выходной файловый поток (ofstream)
// Формат вывода: левый_операнд правый_операнд оператор
// Пример: 2 x * 3 +  (для выражения 2*x+3)
// Преимущество: позволяет вычислять выражение с использованием стека без учета приоритетов
void Tree::to_postfix(std::shared_ptr<Node> node, std::ofstream& out) {
    if (!node) return;
    to_postfix(node->left, out);
    to_postfix(node->right, out);
    out << node->value << " ";
}


// Метод записи дерева в инфиксной форме с полными скобками в файл
// Назначение: обходит дерево в порядке: левое поддерево → корень → правое поддерево
//            и записывает значения узлов в файл, добавляя скобки вокруг каждого оператора
// Параметры:
//   node - умный указатель на текущий узел
//   out - ссылка на выходной файловый поток (ofstream)
// Формат вывода: ((2*x)+3)  (для выражения 2*x+3)
// Примечание: добавляет скобки для всех операторов, что делает порядок вычислений явным,
//             но может приводить к избыточности скобок
void Tree::to_infix(std::shared_ptr<Node> node, std::ofstream& out) {
    if (!node) return;
    out << "(";
    to_infix(node->left, out);
    out << node->value;
    to_infix(node->right, out);
    out << ")";
}

// Метод оптимизации и нормализации дерева
// Назначение: выполняет пост-порядковый обход дерева и применяет
//            оптимизирующие преобразования для унификации представления
// Параметры:
//   node - умный указатель на корень дерева (или поддерева) для преобразования
// Текущее преобразование:
//   Для оператора сложения (+) меняет местами левого и правого потомка,
//   если правый потомок является переменной, а левый - нет
// Цель: унифицировать представление выражений, например, преобразовать "x+2" в "2+x"
//       (всегда ставить константы слева от переменных для оператора сложения)
// Это может упростить последующие оптимизации или сравнение выражений
void Tree::transform(std::shared_ptr<Node> node) {
    if (!node) return;

    transform(node->left);
    transform(node->right);

    if (node->value == "+" &&
        node->left && node->right &&
        node->right->is_variable()) {

        std::swap(node->left, node->right);
    }
}